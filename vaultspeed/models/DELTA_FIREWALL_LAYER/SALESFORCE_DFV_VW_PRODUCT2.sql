		, "PREPJOINBK"."DESCRIPTION" AS "DESCRIPTION"
		, "PREPJOINBK"."ISACTIVE" AS "ISACTIVE"
		, "PREPJOINBK"."CREATEDDATE" AS "CREATEDDATE"
