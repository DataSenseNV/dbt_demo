			, "STG_TEMP_SRC"."LOAD_DATE" AS "LOAD_DATE"
			, "STG_TEMP_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
			, TO_TIMESTAMP('31/12/2999 23:59:59', 'DD/MM/YYYY HH24:MI:SS') AS "LOAD_END_DATE"
			, "STG_TEMP_SRC"."RECORD_TYPE" AS "RECORD_TYPE"
			, 'STG' AS "SOURCE"
			, 1 AS "ORIGIN_ID"
			, CASE WHEN "STG_TEMP_SRC"."JRN_FLAG" = 'D' THEN CAST('Y' AS VARCHAR) ELSE CAST('N' AS VARCHAR) END AS "DELETE_FLAG"
			, "STG_TEMP_SRC"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
			, "STG_TEMP_SRC"."BPTDAY" AS "BPTDAY"
			, "STG_TEMP_SRC"."BPUPMJ" AS "BPUPMJ"
			, "STG_TEMP_SRC"."BPEXDJ" AS "BPEXDJ"
			, "STG_TEMP_SRC"."BPUOM" AS "BPUOM"
			, "STG_TEMP_SRC"."BPCRCD" AS "BPCRCD"
			, "STG_TEMP_SRC"."BPFRMP" AS "BPFRMP"
			, "STG_TEMP_SRC"."BPLOTG" AS "BPLOTG"
			, "STG_TEMP_SRC"."BPCGID" AS "BPCGID"
			, "STG_TEMP_SRC"."BPIGID" AS "BPIGID"
			, "STG_TEMP_SRC"."BPAN8" AS "BPAN8"
			, "STG_TEMP_SRC"."BPLOTN" AS "BPLOTN"
			, "STG_TEMP_SRC"."BPLOCN" AS "BPLOCN"
			, "STG_TEMP_SRC"."BPMCU" AS "BPMCU"
			, "STG_TEMP_SRC"."BPITM" AS "BPITM"
		FROM {{ ref('JDEDWARDS_STG_F4106') }} "STG_TEMP_SRC"
		UNION 
		SELECT 
			  "LKS_SRC"."LNK_ITEMPRICE_PRODUCT_HKEY" AS "LNK_ITEMPRICE_PRODUCT_HKEY"
			, "LNK_SRC"."PRODUCT_HKEY" AS "PRODUCT_HKEY"
			, "LNK_SRC"."ITEMPRICE_HKEY" AS "ITEMPRICE_HKEY"
			, "LKS_SRC"."LOAD_DATE" AS "LOAD_DATE"
			, "LKS_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
			, "LKS_SRC"."LOAD_END_DATE" AS "LOAD_END_DATE"
			, 'SAT' AS "RECORD_TYPE"
			, 'LKS' AS "SOURCE"
			, 0 AS "ORIGIN_ID"
			, "LKS_SRC"."DELETE_FLAG" AS "DELETE_FLAG"
			, "LKS_SRC"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
			, "LKS_SRC"."BPTDAY" AS "BPTDAY"
			, "LKS_SRC"."BPUPMJ" AS "BPUPMJ"
			, "LKS_SRC"."BPEXDJ" AS "BPEXDJ"
			, "LKS_SRC"."BPUOM" AS "BPUOM"
			, "LKS_SRC"."BPCRCD" AS "BPCRCD"
			, "LKS_SRC"."BPFRMP" AS "BPFRMP"
