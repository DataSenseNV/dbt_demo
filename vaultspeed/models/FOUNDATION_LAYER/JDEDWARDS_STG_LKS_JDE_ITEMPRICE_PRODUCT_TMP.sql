	WITH "DIST_STG" AS 
	( 
		SELECT 
			  "STG_DIS_SRC"."ITEMPRICE_HKEY" AS "ITEMPRICE_HKEY"
			, "STG_DIS_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
			, MIN("STG_DIS_SRC"."LOAD_DATE") AS "MIN_LOAD_TIMESTAMP"
		FROM {{ ref('JDEDWARDS_STG_F4106') }} "STG_DIS_SRC"
		GROUP BY  "STG_DIS_SRC"."ITEMPRICE_HKEY",  "STG_DIS_SRC"."LOAD_CYCLE_ID"
	)
	, "TEMP_TABLE_SET" AS 
	( 
		SELECT 
			  "STG_TEMP_SRC"."LNK_ITEMPRICE_PRODUCT_HKEY" AS "LNK_ITEMPRICE_PRODUCT_HKEY"
			, "STG_TEMP_SRC"."PRODUCT_HKEY" AS "PRODUCT_HKEY"
