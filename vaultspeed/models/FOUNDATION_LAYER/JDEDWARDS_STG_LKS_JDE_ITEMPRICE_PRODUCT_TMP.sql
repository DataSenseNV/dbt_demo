		, "TEMP_TABLE_SET"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
		, "TEMP_TABLE_SET"."BPTDAY" AS "BPTDAY"
		, "TEMP_TABLE_SET"."BPUPMJ" AS "BPUPMJ"
		, "TEMP_TABLE_SET"."BPEXDJ" AS "BPEXDJ"
		, "TEMP_TABLE_SET"."BPUOM" AS "BPUOM"
		, "TEMP_TABLE_SET"."BPCRCD" AS "BPCRCD"
		, "TEMP_TABLE_SET"."BPFRMP" AS "BPFRMP"
		, "TEMP_TABLE_SET"."BPLOTG" AS "BPLOTG"
		, "TEMP_TABLE_SET"."BPCGID" AS "BPCGID"
		, "TEMP_TABLE_SET"."BPIGID" AS "BPIGID"
		, "TEMP_TABLE_SET"."BPAN8" AS "BPAN8"
		, "TEMP_TABLE_SET"."BPLOTN" AS "BPLOTN"
		, "TEMP_TABLE_SET"."BPLOCN" AS "BPLOCN"
		, "TEMP_TABLE_SET"."BPMCU" AS "BPMCU"
		, "TEMP_TABLE_SET"."BPITM" AS "BPITM"
	FROM "TEMP_TABLE_SET" "TEMP_TABLE_SET"

