			, "LKS_SRC"."BPIGID" AS "BPIGID"
			, "LKS_SRC"."BPAN8" AS "BPAN8"
			, "LKS_SRC"."BPLOTN" AS "BPLOTN"
			, "LKS_SRC"."BPLOCN" AS "BPLOCN"
			, "LKS_SRC"."BPMCU" AS "BPMCU"
			, "LKS_SRC"."BPITM" AS "BPITM"
		FROM {{ source('DEMO_FL', 'LKS_JDE_ITEMPRICE_PRODUCT') }} "LKS_SRC"
		INNER JOIN {{ source('DEMO_FL', 'LNK_ITEMPRICE_PRODUCT') }} "LNK_SRC" ON  "LKS_SRC"."LNK_ITEMPRICE_PRODUCT_HKEY" = "LNK_SRC"."LNK_ITEMPRICE_PRODUCT_HKEY"
		INNER JOIN "DIST_STG" "DIST_STG" ON  "LNK_SRC"."ITEMPRICE_HKEY" = "DIST_STG"."ITEMPRICE_HKEY"
		WHERE  "LKS_SRC"."LOAD_END_DATE" = TO_TIMESTAMP('31/12/2999 23:59:59' , 'DD/MM/YYYY HH24:MI:SS')
	)
	SELECT 
		  "TEMP_TABLE_SET"."LNK_ITEMPRICE_PRODUCT_HKEY" AS "LNK_ITEMPRICE_PRODUCT_HKEY"
		, "TEMP_TABLE_SET"."PRODUCT_HKEY" AS "PRODUCT_HKEY"
		, "TEMP_TABLE_SET"."ITEMPRICE_HKEY" AS "ITEMPRICE_HKEY"
		, "TEMP_TABLE_SET"."LOAD_DATE" AS "LOAD_DATE"
		, "TEMP_TABLE_SET"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
		, "TEMP_TABLE_SET"."LOAD_END_DATE" AS "LOAD_END_DATE"
		, "TEMP_TABLE_SET"."RECORD_TYPE" AS "RECORD_TYPE"
		, "TEMP_TABLE_SET"."SOURCE" AS "SOURCE"
		, CASE WHEN "TEMP_TABLE_SET"."SOURCE" = 'STG' AND TO_CHAR("TEMP_TABLE_SET"."DELETE_FLAG") || TO_CHAR("TEMP_TABLE_SET"."PRODUCT_HKEY")
			= LAG( TO_CHAR("TEMP_TABLE_SET"."DELETE_FLAG") || TO_CHAR("TEMP_TABLE_SET"."PRODUCT_HKEY"),1)OVER(PARTITION BY "TEMP_TABLE_SET"."ITEMPRICE_HKEY" ORDER BY "TEMP_TABLE_SET"."LOAD_DATE","TEMP_TABLE_SET"."ORIGIN_ID")THEN 1 ELSE 0 END AS "EQUAL"
